function sum($arr) {
    if (empty($arr)) {
        return 0;
    }

    $sum = 0;
    $count = 0;

    foreach ($arr as $elem) {
        if (is_numeric($elem)) {
            $sum += $elem;
            $count++;
        }
    }

    if ($count === 0) {
        return 0;
    }

    return intval($sum / $count);
}

